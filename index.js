const password1 = document.getElementById("value1");
const password2 = document.getElementById("value2");
const generateBtn = document.getElementById("generate");

function getRandomCharacter() {
  const index = Math.floor(Math.random() * characters.length);
  return characters[index];
}

function generatePasswords(length = 15) {
  let pwd1 = "",
    pwd2 = "";
  for (let i = 0; i < length; i++) {
    pwd1 += getRandomCharacter();
    pwd2 += getRandomCharacter();
  }
  password1.textContent = pwd1;
  password2.textContent = pwd2;
}

generateBtn.addEventListener("click", () => {
  generatePasswords();
});
